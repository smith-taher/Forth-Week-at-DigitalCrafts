function showLightbox() {
    document.getElementbyId("commentFormBG").style.display = "block";
    document.getElementbyId("commentFormFG").style.display = "block";
}

function hideLightbox() {
    document.getElementbyId("commentFormBG").style.display = "none";
    document.getElementbyId("commentFormFG").style.display = "none";
}